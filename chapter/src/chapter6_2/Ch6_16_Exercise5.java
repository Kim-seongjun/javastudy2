package chapter6_2;

public class Ch6_16_Exercise5 {
	
	/*
	 Ch6_16_ExerciceExample5 클래스에서 
	 Ch6_16_Exercise5 객체를 생성하고 println() 메소드를
	 호출해서 매개값을 콘솔에 출력하려고 한다. 
	 println()의 매개값은 int, boolean, double, String
	 Ch6_16_Exercise5 클래스에서 println() 메소드를 선언해보기
	 */
	// 작성 위치
	void println(int value) {
		System.out.println(value);
	}
	
	void println(boolean value) {
		System.out.println(value);
	}
	
	void println(double value) {
		System.out.println(value);
	}
	
	void println(String value) {
		System.out.println(value);
	}
}
